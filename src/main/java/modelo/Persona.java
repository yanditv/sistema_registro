package modelo;
import java.time.LocalDate;
import javax.persistence.*;

/**
 *
 * @author Jheyson Gaona
 */
// @Entity(name = "UsuarioEntidad")
@Entity
@Table(name = "usuario")
public class Persona {
    
    @Id
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    private int id;
    
    @Column(nullable = false)
    private String nombre;
    
    @Column(nullable = false)
    private String apellido;
    
    @Column(nullable = false, unique = true)
    private String numIdentificacion;
    
    @Column(nullable = false)
    private String correo;
    
    @Column
    private LocalDate fechaNacimiento;
    
    
    public Persona(){
        
    }
    
    public Persona(String nombre, String apellido, String numIdentificacion,
            String correo, LocalDate fechaNacimiento){
        this.nombre = nombre;
        this.apellido = apellido;
        this.numIdentificacion = numIdentificacion;
        this.correo = correo;
        this.fechaNacimiento = fechaNacimiento;
    }
    
    
    public Persona(int id, String nombre, String apellido, String numIdentificacion, 
            String correo, LocalDate fechaNacimiento){
        this.id = id;
        this.nombre = nombre;
        this.apellido = apellido;
        this.numIdentificacion = numIdentificacion;
        this.correo = correo;
        this.fechaNacimiento = fechaNacimiento;
    }
    
    
    // Getters and Setters
    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getApellido() {
        return apellido;
    }

    public void setApellido(String apellido) {
        this.apellido = apellido;
    }
    
    public String getNumIdentificacion() {
        return numIdentificacion;
    }

    public void setNumIdentificacion(String numIdentificacion) {
        this.numIdentificacion = numIdentificacion;
    }

    public String getCorreo() {
        return correo;
    }

    public void setCorreo(String correo) {
        this.correo = correo;
    }

    public LocalDate getFechaNacimiento() {
        return fechaNacimiento;
    }

    public void setFechaNacimiento(LocalDate fechaNacimiento) {
        this.fechaNacimiento = fechaNacimiento;
    }

    @Override
    public String toString() {
        return "Persona{" + "id=" + id + ", nombre=" + nombre + ", apellido=" + apellido + ", numIdentificacion=" + numIdentificacion + ", correo=" + correo + ", fechaNacimiento=" + fechaNacimiento + '}';
    }
    
    
}